class Food {
  final String name;
  final String description;
  final String image;
  final String price;
  final String rating;
  final String category;
  final bool arAvailable;

  const Food({
    required this.name,
    required this.description,
    required this.image,
    required this.price,
    required this.rating,
    required this.category,
    required this.arAvailable,
  });
}